# Crie um script Python que leia dois números e tente mostrar a soma entre eles
num_1 = int(input('Digite o primeiro valor: \n'))
num_2 = int(input('Digite o segundo valor: \n'))
print('A soma é {}'.format(num_1 + num_2))
print("A soma é:", num_1 + num_2)
print("A soma é: %d!" % (num_1 + num_2))

# Os três formatos de print aceitam operações entre variáveis
